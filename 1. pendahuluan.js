/*
JavaScript adalah bahasa pemrograman tingkat tinggi yang digunakan untuk mengembangkan aplikasi web dan desktop.
Bahasa ini dapat digunakan untuk mengubah perilaku halaman web secara dinamis, seperti menambahkan atau menghapus elemen, mengubah warna atau ukuran teks, menampilkan pesan pop-up, dan banyak lagi.


JavaScript pertama kali diperkenalkan pada tahun 1995 oleh Brendan Eich dan sejak itu menjadi salah satu bahasa pemrograman paling populer di dunia.
Bahasa ini didukung oleh semua browser modern dan dapat digunakan di berbagai platform, termasuk desktop, web, dan mobile.


JavaScript memiliki sintaks yang mirip dengan bahasa pemrograman lainnya seperti Java dan C++.
Namun, perbedaan utamanya adalah bahwa JavaScript adalah bahasa pemrograman berbasis objek prototipe.
Ini berarti bahwa objek di JavaScript dibuat dari prototipe, bukan dari kelas.


JavaScript juga mendukung konsep pemrograman fungsional, yang memungkinkan pengembang untuk memanipulasi data dan menghasilkan hasil dengan cara yang lebih deklaratif dan mudah dipahami.


Untuk mulai menggunakan JavaScript, Anda hanya perlu membuat file JavaScript terpisah atau menambahkan kode JavaScript langsung ke dalam tag <script> pada halaman HTML Anda.
Kode JavaScript akan dijalankan ketika halaman dimuat atau ketika suatu peristiwa tertentu terjadi.


Saat ini, JavaScript juga banyak digunakan untuk mengembangkan aplikasi web modern yang canggih, seperti aplikasi single-page dan progressive web app.
JavaScript juga digunakan dalam pengembangan game dan aplikasi mobile.
 */