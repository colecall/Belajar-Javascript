/*
Operator matematika adalah operator yang digunakan untuk melakukan operasi matematika pada bilangan. 
Operator matematika yang tersedia dalam JavaScript adalah sebagai berikut:

Operator    |	Deskripsi	         |   Contoh
-------------------------------------------------------------------
+	        |   Penjumlahan	         |  5 + 3 = 8
-	        |   Pengurangan	         |  5 - 3 = 2
*	        |   Perkalian	         |  5 * 3 = 15
/	        |   Pembagian	         |  5 / 3 = 1.6666666666666667
%	        |   Modulus (Sisa Bagi)	 |  5 % 3 = 2
**	        |   Pangkat	             |  5 ** 3 = 125
-------------------------------------------------------------------

    Contoh penggunaan operator matematika dalam JavaScript:

    let a = 5;
    let b = 3;

    let c = a + b; // hasilnya adalah 8
    let d = a - b; // hasilnya adalah 2
    let e = a * b; // hasilnya adalah 15
    let f = a / b; // hasilnya adalah 1.6666666666666667
    let g = a % b; // hasilnya adalah 2
    let h = a ** b; // hasilnya adalah 125

    console.log(c);
    console.log(d);
    console.log(e);
    console.log(f);
    console.log(g);
    console.log(h);

Pada contoh kode di atas, variabel a dan b diinisialisasi dengan nilai 5 dan 3, kemudian operator matematika digunakan untuk melakukan operasi penjumlahan, pengurangan, perkalian, pembagian, modulus, dan pangkat pada kedua variabel tersebut. Hasil dari operasi tersebut disimpan dalam variabel c, d, e, f, g, dan h.

*/