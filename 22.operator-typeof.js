/*
Operator typeof adalah operator pada JavaScript yang digunakan untuk mengembalikan tipe data dari sebuah nilai atau variabel. 
typeof mengembalikan nilai string yang menunjukkan jenis tipe data dari nilai atau variabel yang diberikan.

    Contoh penggunaan operator typeof:
    let x = 42;
    console.log(typeof x); // Output: "number"

    let y = "Hello, world!";
    console.log(typeof y); // Output: "string"

    let z = true;
    console.log(typeof z); // Output: "boolean"

    let a = undefined;
    console.log(typeof a); // Output: "undefined"

    let b = null;
    console.log(typeof b); // Output: "object"

Dalam contoh di atas, typeof digunakan untuk mengembalikan tipe data dari variabel x, y, z, a, dan b. Variabel x memiliki tipe data "number", variabel y memiliki tipe data "string", variabel z memiliki tipe data "boolean", variabel a memiliki tipe data "undefined", dan variabel b memiliki tipe data "object".

Perlu diingat bahwa typeof null mengembalikan "object", bukan "null", karena adanya kesalahan dalam implementasi asli JavaScript.
*/